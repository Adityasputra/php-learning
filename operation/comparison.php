<?php

$a = 10;
$b = 20;

// Equal
var_dump($a == $b);

// Identical
var_dump($a === $b);

// Not Equal
var_dump($a != $b);

// Not Equal
var_dump($a <> $b);

// Not Identical
var_dump($a !== $b);

// Less Than
var_dump($a < $b);

// Greater Than
var_dump($a > $b);

// Less Than or Equal to
var_dump($a <= $b);

// Greater Than or Equal to
var_dump($a >= $b);

// Spaceship
var_dump($a <=> $b);